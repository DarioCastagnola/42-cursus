#include "so_long.h"

int	is_in_set(char c)
{
	char	*set;
	int		i;

	set = "01CPEFG";
	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}
